
# packing => assigning value to the tuple
# unpacking => extracting a value of a tuple

# assigned value to a tuple, this is packing
cars = ("Audi","BMW","Mini Cooper","Benz")

# unpacking => extracting this value from a tuple and assign them to a variable
# declare the variable for each item in the tuple => 4 variable for cars tuple

# this assignment expression will allow us to unpack a tuple
car1,car2,car3,car4 = cars
print(car1)
print(car2)
print(car3)
print(car4)
